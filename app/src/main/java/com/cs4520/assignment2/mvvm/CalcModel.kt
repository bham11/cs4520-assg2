package com.cs4520.assignment2.mvvm

class CalcModel {
     fun add(x:Double, y:Double): Double {
        return x + y
    }

     fun subtract(x:Double, y:Double): Double {
        return x - y
    }

     fun multiply(x:Double, y:Double): Double {
        return x * y
    }

     fun divide(x:Double, y:Double): Double {
        return x / y
    }
}