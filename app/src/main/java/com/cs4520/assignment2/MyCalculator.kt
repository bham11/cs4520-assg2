package com.cs4520.assignment2

object MyCalculator {

    fun add(x:Float, y:Float): Float {
        return x + y
    }

    fun subtract(x:Float, y:Float): Float {
        return x - y
    }

    fun multiply(x:Float, y:Float): Float {
        return x * y
    }

    fun divide(x:Float, y:Float): Float {
        return x / y
    }
}